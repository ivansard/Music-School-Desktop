    private void jbtnCreatePurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCreatePurchaseActionPerformed
        Client selectedClient = (Client) jComboClients.getSelectedItem();
        Course selectedCourse = (Course) jComboCourses.getSelectedItem();

        if (selectedClient != null && selectedCourse != null) {
            Purchase createdPurhcase = new Purchase(selectedCourse, selectedClient, new Date());
            Purchase insertedPurhcase = Controller.getInstance().insertPurhcase(createdPurhcase);
            if (insertedPurhcase != null) {
                JOptionPane.showMessageDialog(this, "Successful creation of purchase!");
                this.dispose();
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Issue in creation of purchase!");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a client and a course!");
            return;
        }
    }//GEN-LAST:event_jbtnCreatePurchaseActionPerformed

