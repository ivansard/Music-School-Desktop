    private void populateComboClients() {
        List<Client> allClients = Controller.getInstance().getAllClients();
        for (Client client : allClients) {
            jComboClients.addItem(client);
        }
        jComboClients.setSelectedItem(null);
    }

    private void populateComboCourses() {
        List<Course> allCourses = Controller.getInstance().getAllCourses();
        for (Course course : allCourses) {
            jComboCourses.addItem(course);
        }
        jComboCourses.setSelectedItem(null);
    }
