    private void jbtnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDetailsActionPerformed
        int selectedIndex = jtblCourses.getSelectedRow();
        if (selectedIndex > -1) {
            AllCoursesTableModel tableModel = (AllCoursesTableModel) jtblCourses.getModel();
            Course selectedCourse = tableModel.getCourses().get(selectedIndex);

            Session.getInstance().getMap().put("selected_course", selectedCourse);
            JDialog courseDetails = new FUpdateDeleteCourse(null, true);
            courseDetails.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a course to see details!");
            return;
        }
    }//GEN-LAST:event_jbtnDetailsActionPerformed

