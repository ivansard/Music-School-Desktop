    private void populateTableAllCourses() {
        List<Course> allCourses = Controller.getInstance().getAllCourses();
        AllCoursesTableModel tableModel = new AllCoursesTableModel(allCourses);
        jtblCourses.setModel(tableModel);
    }

    private void prepareForSort() {
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(jtblCourses.getModel());
        jtblCourses.setRowSorter(rowSorter);

        jtxtNameSearchCourse.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent de) {
                String filterPurchase = jtxtNameSearchCourse.getText();
                if (filterPurchase.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + filterPurchase));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent de) {
                String text = jtxtNameSearchCourse.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent de) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }
